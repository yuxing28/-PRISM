// ============ API 处理 ============

export async function POST(req: NextRequest) {
    try {
        const { messages, isDebateMode, decisionMode = 'standard', apiKey: clientApiKey, userMemory } = await req.json();
        const API_KEY = clientApiKey || process.env.DEEPSEEK_API_KEY;

        if (!API_KEY) {
            return NextResponse.json({
                error: "API Key Missing",
                details: "请在用户设置中配置您的 DeepSeek API Key，或者在服务器端配置环境变量。"
            }, { status: 401 });
        }

        // 选择 System Prompt
        let systemPrompt = isDebateMode 
            ? PROMPTS.redTeam 
            : PROMPTS[decisionMode as keyof typeof PROMPTS] || PROMPTS.standard;

        // 注入用户画像（如果有）
        if (userMemory && userMemory.decisionStyle !== 'unknown') {
            const userProfileSection = buildUserProfilePrompt(userMemory);
            systemPrompt = userProfileSection + '\n\n' + systemPrompt;
        }

        const fullMessages = [
            { role: "system", content: systemPrompt },
            ...messages
        ];

        const response = await fetch(API_URL, {
            method: "POST",
            headers: {
                "Content-Type": "application/json",
                "Authorization": `Bearer ${API_KEY}`,
            },
            body: JSON.stringify({
                model: "deepseek-chat",
                messages: fullMessages,
                stream: true,
            }),
        });

        if (!response.ok) {
            const errorText = await response.text();
            return NextResponse.json({ error: "API Error", details: errorText }, { status: response.status });
        }

        return new Response(response.body, {
            headers: {
                "Content-Type": "text/event-stream",
                "Cache-Control": "no-cache",
                "Connection": "keep-alive",
            },
        });

    } catch (error) {
        console.error("Chat Error:", error);
        return NextResponse.json({ error: "Internal Server Error" }, { status: 500 });
    }
}

// 构建用户画像 Prompt
function buildUserProfilePrompt(userMemory: {
    decisionStyle: string;
    riskTolerance: number;
    frequentDomains: string[];
    keyDecisions: Array<{
        topic: string;
        result: string;
        score: number;
        riskLevel: string;
        date: number;
    }>;
    preferredMode: string;
}): string {
    const styleMap: Record<string, string> = {
        'conservative': '保守型（偏好低风险、稳健的选择）',
        'neutral': '中性型（风险与收益平衡）',
        'aggressive': '激进型（愿意承担较高风险追求高回报）',
    };

    const parts: string[] = ['【用户画像 - 基于历史对话学习】'];
    
    // 决策风格
    if (userMemory.decisionStyle !== 'unknown') {
        parts.push(`决策风格：${styleMap[userMemory.decisionStyle] || userMemory.decisionStyle}`);
    }
    
    // 风险承受度
    parts.push(`风险承受度：${userMemory.riskTolerance}/10`);
    
    // 常见领域
    if (userMemory.frequentDomains.length > 0) {
        parts.push(`常咨询领域：${userMemory.frequentDomains.slice(0, 5).join('、')}`);
    }
    
    // 历史决策摘要
    if (userMemory.keyDecisions.length > 0) {
        const recentDecisions = userMemory.keyDecisions.slice(0, 3);
        const summaries = recentDecisions.map(d => 
            `- ${d.topic}（评分${d.score}/10，${d.riskLevel}，建议${d.result}）`
        );
        parts.push(`近期决策记录：\n${summaries.join('\n')}`);
    }

    parts.push('');
    parts.push('【应用规则】根据用户画像调整分析风格：');
    parts.push('- 保守型用户：更强调风险，建议更谨慎');
    parts.push('- 激进型用户：可以更多讨论机会，但仍需指出风险');
    parts.push('- 参考历史决策，保持建议的一致性');

    return parts.join('\n');
}
